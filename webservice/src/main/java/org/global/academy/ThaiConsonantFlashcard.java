package org.global.academy;

public class ThaiConsonantFlashcard {

    private String front;
    private String back;

    public ThaiConsonantFlashcard(String front, String back) {
        this.front = front;
        this.back = back;
    }

    public String getFront() {
        return front;
    }

    public String getBack() {
        return back;
    }
}
